# Participation (POV)
# ---------------------------------------------------------------------------


class ParticipationAlgo(ExecutionAlgo):
    """Trade as a fixed percentage of market volume.

    Parameters
    ----------
    parent_order : ParentOrder
    target_rate : float
        Target participation as fraction of volume (e.g. 0.10 = 10%).
    expected_adv : int
        Expected average daily volume for scheduling.
    check_interval_minutes : int
        How often to place slices.
    """

    def __init__(
        self,
        parent_order: ParentOrder,
        target_rate: float = 0.10,
        expected_adv: int = 1_000_000,
        check_interval_minutes: int = 5,
    ):
        super().__init__(parent_order)
        self.target_rate = target_rate
        self.expected_adv = expected_adv
        self.check_interval_minutes = check_interval_minutes

    @property
    def name(self) -> str:
        return "POV"

    def generate_schedule(self) -> list[ChildSlice]:
        po = self.parent_order
        interval = timedelta(minutes=self.check_interval_minutes)
        trading_day_minutes = 390

        # Expected volume per interval
        vol_per_interval = (
            self.expected_adv
            * self.check_interval_minutes
            / trading_day_minutes
        )
        target_qty_per_interval = int(vol_per_interval * self.target_rate)
        target_qty_per_interval = max(1, target_qty_per_interval)

        n_intervals = max(1, int(po.duration_seconds / 60 / self.check_interval_minutes))

        schedule = []
        remaining = po.total_qty

        for i in range(n_intervals):
            qty = min(target_qty_per_interval, remaining)
            if qty <= 0:
                break

            remaining -= qty
            schedule.append(ChildSlice(
                slice_id=i,
                target_time=po.start_time + i * interval,
                target_qty=qty,
                is_final=(remaining == 0),
            ))

            if remaining == 0:
                break

        # If remaining, add final slice
        if remaining > 0:
            schedule.append(ChildSlice(
                slice_id=len(schedule),
                target_time=po.end_time,
                target_qty=remaining,
                is_final=True,
            ))

        return schedule


# ---------------------------------------------------------------------------
# Factory
# ---------------------------------------------------------------------------


def create_algo(algo_name: str, parent_order: ParentOrder, **kwargs) -> ExecutionAlgo:
    """Factory for creating execution algorithms.

    Parameters
    ----------
    algo_name : str
        One of: "TWAP", "VWAP", "IS", "POV"
    parent_order : ParentOrder
    **kwargs : Algorithm-specific parameters.
    """
    algos = {
        "TWAP": TWAPAlgo,
        "VWAP": VWAPAlgo,
        "IS": ISAlgo,
        "POV": ParticipationAlgo,
    }
    key = algo_name.upper()
    if key not in algos:
        raise ValueError(f"Unknown algo: {algo_name}. Choose from {list(algos.keys())}")
    return algos[key](parent_order, **kwargs)
